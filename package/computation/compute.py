#------------------------------------------------------------------------------#
# compute cosine similarity between two vectors                                #
#------------------------------------------------------------------------------#
def cosine_similarity(v1, v2):

  return np.dot(v1, v2) / (np.sqrt(np.dot(v1, v1)) * np.sqrt(np.dot(v2, v2)))
