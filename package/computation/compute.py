#------------------------------------------------------------------------------#
# get top n movies which can be recommended to user                            #
#------------------------------------------------------------------------------#
def get_top_n_movies(m_dict, mat, u_id, top_n):

  m_dict = sorted(m_dict.items(), key = lambda arg: arg[1], reverse = True)
  top_n_list = []
  c = 0
  for k, v in m_dict:
    if mat[u_id, k] == 0.0:
      top_n_list.append(k)
      c += 1
    if c == top_n:
      break

  return top_n_list
